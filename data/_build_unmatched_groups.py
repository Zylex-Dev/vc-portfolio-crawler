def fmt(x):
    return "" if pd.isna(x) else str(x)


def main():
    df = pd.read_csv("data/startup_agent_assignment.csv")
    un = df[df["relevance"] < 7].copy()

    assert len(ASSIGN) == len(un), f"mapping {len(ASSIGN)} != unmatched {len(un)}"
    missing = set(un["id"]) - set(ASSIGN)
    assert not missing, f"unassigned ids: {sorted(missing)}"

    un["group_id"] = un["id"].map(lambda i: ASSIGN[i][0])
    un["functional_group"] = un["group_id"].map(lambda g: GROUPS[g][0])
    un["group_rationale"] = un["id"].map(lambda i: ASSIGN[i][1])

    # --- per-startup CSV ---
    per = un[["id", "fund", "name", "website", "sectors", "pmo_score", "relevance",
              "group_id", "functional_group", "group_rationale"]].copy()
    per = per.sort_values(["group_id", "relevance"], ascending=[True, False])
    per.to_csv("data/unmatched_groups.csv", index=False)

    # --- per-group summary CSV (agent_startups.csv style) ---
    rows = []
    for gid, (gname, gdesc) in GROUPS.items():
        sub = un[un["group_id"] == gid].sort_values("relevance", ascending=False)
        startups = "; ".join(f"{n} ({r})" for n, r in zip(sub["name"], sub["relevance"]))
        rows.append({
            "group_id": gid,
            "group_name": gname,
            "description": gdesc,
            "num_startups": len(sub),
            "avg_pmo_score": round(sub["pmo_score"].mean(), 2),
            "avg_relevance": round(sub["relevance"].mean(), 2),
            "startups": startups,
        })
    summ = pd.DataFrame(rows).sort_values("num_startups", ascending=False)
    summ.to_csv("data/unmatched_group_summary.csv", index=False)

    # --- markdown analysis ---
    total = len(un)
    rel0 = int((un["relevance"] == 0).sum())
    near = int(((un["relevance"] >= 1) & (un["relevance"] <= 6)).sum())
    lines = []
    lines.append("# Анализ unmatched-стартапов (relevance < 7)\n")
    lines.append(
        f"Из 518 EdTech-стартапов, прошедших агент-матчинг, **{total} не сметчились ни с "
        f"одним из 44 ИИ-агентов** (релевантность < 7) и, как следствие, не попали ни в одно "
        "из 5 средств ПМО. Ниже — их разбиение на функциональные группы.\n")
    lines.append("## Две причины «несовпадения»\n")
    lines.append(
        f"- **{rel0} стартапов с релевантностью 0** — полностью вне «внутриклассного» учебного "
        "контура, на который был настроен матчер (финансы, наём, админка, телемедицина, "
        "маркетплейсы, железо, фандрайзинг).\n"
        f"- **{near} near-miss (1–6)** — это реальные обучающие продукты, которые либо слишком "
        "широки (целые платформы/школы), либо делают учебную функцию, для которой у нас пока нет "
        "отдельного агента (разговорная языковая практика, аналитика диалога в классе и т.п.).\n")
    lines.append("## Функциональные группы\n")
    lines.append("| Группа | Название | Стартапов | Доля | Ср. pmo | Ср. relevance |")
    lines.append("|--------|----------|-----------|------|---------|---------------|")
    for _, r in summ.iterrows():
        share = f"{100 * r['num_startups'] / total:.0f}%"
        lines.append(f"| {r['group_id']} | {r['group_name']} | {r['num_startups']} | "
                     f"{share} | {r['avg_pmo_score']} | {r['avg_relevance']} |")
    lines.append("")
    lines.append("## Состав групп (по 3 примера)\n")
    for gid, (gname, gdesc) in GROUPS.items():
        sub = un[un["group_id"] == gid].sort_values("relevance", ascending=False)
        ex = ", ".join(sub["name"].head(3))
        lines.append(f"- **{gid} — {gname}** ({len(sub)}): {gdesc} _Напр.: {ex}._")
    lines.append("")
    lines.append("## Куда смотреть для новых идей (агентов / средств ПМО)\n")
    lines.append(
        "Среди near-miss есть учебные функции, которых нет в текущем каталоге 44 агентов — "
        "это кандидаты в новые агенты/средства:\n"
        "- **Разговорная языковая практика с AI** (Speak, Edwin, vocal, Iago) — AI-собеседник "
        "с мгновенной устной обратной связью.\n"
        "- **Аналитика диалога в классе** (TeachFX) — обратная связь учителю о вовлечённости и "
        "характере общения.\n"
        "- **Вовлечение семьи и коммуникация** (ClassDojo, Family Engagement Lab, Dost) — мост "
        "школа↔семья, отсутствующий в средствах ПМО.\n"
        "- **Благополучие и SEL** (Wayfinder, Social Optics, EveryDay Labs) — социально-"
        "эмоциональное развитие и раннее реагирование.\n"
        "- **Развитие и поддержка педагога** (Uppercase, Brisk, Teachy) — AI-ассистенты для "
        "учителя за пределами генерации заданий.\n")
    lines.append(
        "Группы G1–G3 и G5 (платформы, репетиторы, карьера, админка/финансы) — это в основном "
        "**бизнес-модели и инфраструктура**, а не педагогические средства: они закономерно вне "
        "ПМО и для генерации новых агентов менее интересны, чем near-miss из G4 и G6.\n")
    with open("data/unmatched_analysis.md", "w") as f:
        f.write("\n".join(lines))

    print(f"unmatched: {total} | rel0: {rel0} | near-miss: {near}")
    print(summ[["group_id", "group_name", "num_startups", "avg_pmo_score", "avg_relevance"]]
          .to_string(index=False))


if __name__ == "__main__":
    main()
